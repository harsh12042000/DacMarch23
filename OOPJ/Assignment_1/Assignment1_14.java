/*
Write a program to convert state of Short instance into byte, 
short, int, long, float and double.
 */
public class Assignment1_14 {
    public static void main(String[] args) {
        
        Short s = new Short((short)5);

        System.out.println("Short instance into byte: " + s.shortValue());
        System.out.println("Short instance into int: " + s.intValue());
        System.out.println("Short instance into long: " + s.longValue());
        System.out.println("Short instance into float: " + s.shortValue());
        System.out.println("Short instance into byte: " + s.shortValue());
    }
    
}
