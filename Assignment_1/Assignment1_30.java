/*
Write a program to find minimum and maximum number as well as 
to add two double numbers using methods of Double.
 */
public class Assignment1_30 {
    public static void main(String[] args) {
        
        double a = 222223.25;
        double b = 661411.12222221;

        System.out.println("Minimum Number : " + Double.min(a, b));
        System.out.println("Maximum Number : " + Double.max(a, b));
        System.out.println("Addition : " + Double.sum(a, b));

    }
}
