/*
 Write a program to find minimum and maximum number as well as 
to add two integer numbers using methods of Integer
 */
public class Assignment1_18 {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 20;

        System.out.println("Minimum Number : " + Integer.min(a, b));
        System.out.println("Maximum Number : " + Integer.max(a, b));
        System.out.println("Addition : " + Integer.sum(a, b));
    }
}
